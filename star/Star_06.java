package java04_control.star;

public class Star_06 {
	public static void main(String[] args) {
		
		//12345
		//23456
		//34567
		//45678
		//56789
		
		for(int i=0; i<5; i++) { //i青, 0~4, x5
			for(int j=0; j<5; j++) { //j青, 0~4, x5
				System.out.print(i+1 +j); //青+凯
			}
			System.out.println(); //青付促 俺青
		}
		
	}
}
