		// 처음 do 키워드를 만나면 조건 검사없이 반복코드를 수행한다
		// 이후 조건 검사를 하고 true/false 에 따라 반복 수행한다
		
		//-> 최초 1회를 무조건 실행하고 다음부터 반복하는 Loop
		//------------------------------------------------------
		//메뉴 선택처럼 최초 1회를 수행해야 다음 작업을 알 수 있는 것들
