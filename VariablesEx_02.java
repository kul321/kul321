package java01_variables;//패키지 선언

public class VariablesEx_02 {//클래스 정의
	public static void main(String[] args) {//main()
		
		int num; //int형 변수 선언 - int형 변수 공간 생성하기
		
		num = 13;//num변수에 13값 대입 - 변수 공간에 데이터 저장하기
		
		System.out.println( num ); //변수의 값불러와서 출력하기
	}//main() end
}//VariablesEx_02 class end
